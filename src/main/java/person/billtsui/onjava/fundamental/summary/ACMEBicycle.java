package person.billtsui.onjava.fundamental.summary;

public class ACMEBicycle implements IBicycle {

    int cadence = 0;
    int speed = 0;
    int gear = 1;

    // The compiler will now require that methods
    // changeCadence, changeGear, speedUp, and applyBrakes
    // all be implemented. Compilation will fail if
    // these methods are missing from this class.
    
    public void changeCadence(int newValue) {
        cadence = newValue;
    }

    public void changeGear(int newValue) {
        gear = newValue;
    }

    public void speedUp(int increment) {
        speed += increment;
    }

    public void applyBrakes(int decrement) {
        speed -= decrement;
    }

    void printStates() {
        IO.println("cadence: " + cadence + " speed: "
                + speed + " gear: " + gear);
    }
}
